    //Checks if the dial code is valid or not
    public boolean isValidDialCode(String dialcode) {
        int countryDialCodeLength = countryDialCodes.length;
        boolean flag = false;
        for (int i = 0; i < countryDialCodeLength; i++) {
            if (dialcode.equalsIgnoreCase(countryDialCodes[i]))
                flag = true;
        }

        return flag;
    }



